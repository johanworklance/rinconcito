    private void btnFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFotoActionPerformed
        // TODO add your handling code here:
        JFileChooser elegirFoto= new JFileChooser();
        elegirFoto.setFileSelectionMode(JFileChooser.FILES_ONLY);
        //con esto nuestro seleccionador de imagenes solo vera archivos
        //y no carpetas
        FileNameExtensionFilter filtro= new FileNameExtensionFilter("fotos","png","jpg","jpeg");
        //el filtro que usaremos es los tipos de archivo que enviamos al constructor
        elegirFoto.setFileFilter(filtro);
        int res= elegirFoto.showOpenDialog(this);//este metodo es el que
        //abre la ventana para escoger archivo, ese this, es el componente padre
        //desde donde se mostrara la ventana, en este caso adelante del boton foto
        if(res == elegirFoto.APPROVE_OPTION){//si le dio a aceptar
            File archivo= elegirFoto.getSelectedFile();
            
            nombreFoto= archivo.getName();
            lbArchivo.setText(nombreFoto);
        }
    }//GEN-LAST:event_btnFotoActionPerformed

